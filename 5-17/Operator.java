public class Operator {
    public static void main(String[] args){
        int a = 28;
        int b = 7;
        int c = 10;

        // addition
        System.out.println(a + b);
        // subtraction
        System.out.println(a - b);
        // multiplcation
        System.out.println(a * b);
        // division
        System.out.println(a / b);
        // remainder
        System.out.println(c % b);
        // increment
        System.out.println(++a);


        // not an operator, just a reset
        a = 28;
        // decrement

        
        System.out.println(--a);
    }
}
